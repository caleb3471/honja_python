input_num = int(input("정수를 입력하세요."))

if input_num%2 == 0:
    print("{}는 2로 나누어 떨어지는 숫자입니다.".format(input_num))
else:
    print("{}는 2로 나누어 떨어지는 숫자가 아닙니다.".format(input_num))
    

if input_num%3 == 0:
    print("{}는 3로 나누어 떨어지는 숫자입니다.".format(input_num))
else:
    print("{}는 3로 나누어 떨어지는 숫자가 아닙니다.".format(input_num))
    

if input_num%4 == 0:
    print("{}는 4로 나누어 떨어지는 숫자입니다.".format(input_num))
else:
    print("{}는 4로 나누어 떨어지는 숫자가 아닙니다.".format(input_num))
    

if input_num%5 == 0:
    print("{}는 5로 나누어 떨어지는 숫자입니다.".format(input_num))
else:
    print("{}는 5로 나누어 떨어지는 숫자가 아닙니다.".format(input_num))